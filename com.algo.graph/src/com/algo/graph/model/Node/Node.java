package com.algo.graph.model.Node;

public class Node {
	
	private int id;
	private String name;

	public Node(int id) {
		this.id=id;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

}
